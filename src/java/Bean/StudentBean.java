package Bean;

/**
 *
 * @author jack1
 */
public class StudentBean {

    
   private int ID;
   private String email;
   private String name;
   private int month;
   private int yearStarts;
   private String major;
   private String program;
   
   //Construction for student, attributes are student's info
   public StudentBean(){
       
   }
    /**
     * @return the ID
     */
    public int getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(int ID) {
        this.ID = ID;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the month
     */
    public int getMonth() {
        return month;
    }

    /**
     * @param month the month to set
     */
    public void setMonth(int month) {
        this.month = month;
    }

    /**
     * @return the yearStarts
     */
    public int getYear() {
        return yearStarts;
    }

    /**
     * @param yearStarts the yearStarts to set
     */
    public void setYearStarts(int yearStarts) {
        this.yearStarts = yearStarts;
    }

    /**
     * @return the major
     */
    public String getMajor() {
        return major;
    }

    /**
     * @param major the major to set
     */
    public void setMajor(String major) {
        this.major = major;
    }

    /**
     * @return the program
     */
    public String getProgram() {
        return program;
    }

    /**
     * @param program the program to set
     */
    public void setProgram(String program) {
        this.program = program;
    }
    
    
 
}
